    private void loadEmployees() {
        try {
            List<Object[]> employees = sessionManager.getAllEmployees();
            if (employees == null) {
                JOptionPane.showMessageDialog(this, "Không thể tải danh sách nhân viên!", 
                    "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) employeesTable.getModel();
            model.setRowCount(0);
            
            for (Object[] emp : employees) {
                if (emp.length >= 2) {
                    model.addRow(new Object[]{
                        emp[0], // ID
                        emp[1]  // Tên nhân viên
                    });
                }
            }
            
        } catch (Exception e) {
            logger.error("Error loading employees", e);
            JOptionPane.showMessageDialog(this, "Lỗi khi tải danh sách nhân viên: " + e.getMessage(), 
                "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    
