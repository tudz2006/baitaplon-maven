    private void startSessionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startSessionButtonActionPerformed
        int selectedRow = employeesTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên!", 
                "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Integer employeeId = (Integer) employeesTable.getValueAt(selectedRow, 0);
        String employeeName = (String) employeesTable.getValueAt(selectedRow, 1);
        
        if (sessionManager.startSession(employeeId)) {
            sessionStarted = true;
            JOptionPane.showMessageDialog(this, 
                "Đã bắt đầu ca làm việc cho nhân viên: " + employeeName, 
                "Thành công", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Không thể bắt đầu ca làm việc!", 
                "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_startSessionButtonActionPerformed

